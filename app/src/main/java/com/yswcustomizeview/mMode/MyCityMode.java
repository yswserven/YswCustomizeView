public class MyCityMode {
    private int id;
    private String addres_name;
    private int pid;
    private int level;
    private String jd_code;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getAddres_name() {
        return addres_name;
    }

    public void setAddres_name(String addres_name) {
        this.addres_name = addres_name;
    }

    public int getPid() {
        return pid;
    }

    public void setPid(int pid) {
        this.pid = pid;
    }

    public int getLevel() {
        return level;
    }

    public void setLevel(int level) {
        this.level = level;
    }

    public String getJd_code() {
        return jd_code;
    }

    public void setJd_code(String jd_code) {
        this.jd_code = jd_code;
    }
}
